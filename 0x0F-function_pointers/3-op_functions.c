#include "3-calc.h"

/**
 * op_add - addition
 * @a: first int
 * @b: second int
 * Return: result
 */

int op_add(int a, int b)
{
	return (a + b);
}
/**
 * op_sub - subtraction
 * @a: first int
 * @b: second it
 * Return: result
 */
int op_sub(int a, int b)
{
	return (a - b);
}
/**
 * op_mul - multiplication
 * @a: first int
 * @b: second int
 * Return: result
 */

int op_mul(int a, int b)
{
	return (a * b);
}
/**
 * op_div - division of 2 numbers
 * @a: first int
 * @b: second int
 * Return: result
 */

int op_div(int a, int b)
{
	return (a / b);
}
/**
 * op_mod - modulus
 * @a: first int
 * @b: second int
 * Return: remainder of division
 */

int op_mod(int a, int b)
{
	return (a % b);
}
